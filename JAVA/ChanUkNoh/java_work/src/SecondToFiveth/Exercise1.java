package SecondToFiveth;

public class Exercise1 {
    public static void main(String[] args) {

        // 1 ~ 100까지의 숫자를 모두 더해서 출력해보자
        int i;
        int sum = 0;

        for(i = 1; i <= 100; i++) {
            sum += 1;
        }
        System.out.println("1 ~ 100 까지의 합 : " + sum);
    }
}
