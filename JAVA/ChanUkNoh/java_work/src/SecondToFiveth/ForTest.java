package SecondToFiveth;

public class ForTest {
    public static void main(String[] args) {
        int i;

        for(i = 0; i < 3; i++) {
            System.out.println("i = " + i);
        }

        for(int j = 0; j < 4; j++) {
            System.out.println("j = " + j);
        }

        System.out.println("나는 여기서도 나와" + i);
    }
}
